package movie.common.bean;

import java.util.HashMap;

public class Item {
	private String type = new String();
	private String version = new String();
	private Object basic = new Object();
	private HashMap<String, Item_data> data = new HashMap<String,Item_data>();
	private Object groups = new Object();
	private Object tree = new Object();
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public Object getBasic() {
		return basic;
	}
	public void setBasic(Object basic) {
		this.basic = basic;
	}
	public HashMap<String, Item_data> getData() {
		return data;
	}
	public void setData(HashMap<String, Item_data> data) {
		this.data = data;
	}
	public Object getGroups() {
		return groups;
	}
	public void setGroups(Object groups) {
		this.groups = groups;
	}
	public Object getTree() {
		return tree;
	}
	public void setTree(Object tree) {
		this.tree = tree;
	}
}
