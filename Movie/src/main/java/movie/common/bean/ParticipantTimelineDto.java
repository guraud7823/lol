package movie.common.bean;

import java.util.HashMap;

public class ParticipantTimelineDto {
	private String lane = new String(); //Participant's calculated lane. MID and BOT are legacy values.
	private int participantId;
	private HashMap<String,Double> csDiffPerMinDeltas = new HashMap(); //Creep score difference versus the calculated lane opponent(s) for a specified period. 
	private HashMap<String,Double> goldPerMinDeltas = new HashMap(); //Gold for a specified period. 
	private HashMap<String,Double> xpDiffPerMinDeltas = new HashMap(); //Experience difference versus the calculated lane opponent(s) for a specified period. 
	private HashMap<String,Double> creepsPerMinDeltas = new HashMap(); // Creeps for a specified period.  
	private HashMap<String,Double> xpPerMinDeltas = new HashMap(); //Experience change for a specified period. 
	private String role = new String(); // Participant's calculated role. 
	private HashMap<String,Double> damageTakenDiffPerMinDeltas = new HashMap(); //Damage taken difference versus the calculated lane opponent(s) for a specified period. 
	private HashMap<String,Double> damageTakenPerMinDeltas = new HashMap(); //Damage taken for a specified period. 
	public String getLane() {
		return lane;
	}
	public void setLane(String lane) {
		this.lane = lane;
	}
	public int getParticipantId() {
		return participantId;
	}
	public void setParticipantId(int participantId) {
		this.participantId = participantId;
	}
	public HashMap<String, Double> getCsDiffPerMinDeltas() {
		return csDiffPerMinDeltas;
	}
	public void setCsDiffPerMinDeltas(HashMap<String, Double> csDiffPerMinDeltas) {
		this.csDiffPerMinDeltas = csDiffPerMinDeltas;
	}
	public HashMap<String, Double> getGoldPerMinDeltas() {
		return goldPerMinDeltas;
	}
	public void setGoldPerMinDeltas(HashMap<String, Double> goldPerMinDeltas) {
		this.goldPerMinDeltas = goldPerMinDeltas;
	}
	public HashMap<String, Double> getXpDiffPerMinDeltas() {
		return xpDiffPerMinDeltas;
	}
	public void setXpDiffPerMinDeltas(HashMap<String, Double> xpDiffPerMinDeltas) {
		this.xpDiffPerMinDeltas = xpDiffPerMinDeltas;
	}
	public HashMap<String, Double> getCreepsPerMinDeltas() {
		return creepsPerMinDeltas;
	}
	public void setCreepsPerMinDeltas(HashMap<String, Double> creepsPerMinDeltas) {
		this.creepsPerMinDeltas = creepsPerMinDeltas;
	}
	public HashMap<String, Double> getXpPerMinDeltas() {
		return xpPerMinDeltas;
	}
	public void setXpPerMinDeltas(HashMap<String, Double> xpPerMinDeltas) {
		this.xpPerMinDeltas = xpPerMinDeltas;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	public HashMap<String, Double> getDamageTakenDiffPerMinDeltas() {
		return damageTakenDiffPerMinDeltas;
	}
	public void setDamageTakenDiffPerMinDeltas(HashMap<String, Double> damageTakenDiffPerMinDeltas) {
		this.damageTakenDiffPerMinDeltas = damageTakenDiffPerMinDeltas;
	}
	public HashMap<String, Double> getDamageTakenPerMinDeltas() {
		return damageTakenPerMinDeltas;
	}
	public void setDamageTakenPerMinDeltas(HashMap<String, Double> damageTakenPerMinDeltas) {
		this.damageTakenPerMinDeltas = damageTakenPerMinDeltas;
	}
}
