package movie.common.bean;

import java.util.ArrayList;

public class MatchDto {
	private int seasonId;
	private int queueId;
	private long gameId;
	private ArrayList<ParticipantIdentityDto> participantIdentities = new ArrayList(); //Participant identity information. 
	private String gameVersion = new String(); // The major.minor version typically indicates the patch the match was played on.  
	private String platformId = new String(); //Platform where the match was played. 
	private String gameMode = new String();
	private int mapId;
	private String gameType = new String();
	private ArrayList<TeamStatsDto> teams = new ArrayList(); // Team information.  
	private ArrayList<ParticipantDto> participants = new ArrayList(); // Participant information.  
	private long gameDuration; // Match duration in seconds. 
	private long gameCreation; // Designates the timestamp when champion select ended and the loading screen appeared, NOT when the game timer was at 0:00.  
	public int getSeasonId() {
		return seasonId;
	}
	public void setSeasonId(int seasonId) {
		this.seasonId = seasonId;
	}
	public int getQueueId() {
		return queueId;
	}
	public void setQueueId(int queueId) {
		this.queueId = queueId;
	}
	public long getGameId() {
		return gameId;
	}
	public void setGameId(long gameId) {
		this.gameId = gameId;
	}
	public ArrayList<ParticipantIdentityDto> getParticipantIdentities() {
		return participantIdentities;
	}
	public void setParticipantIdentities(ArrayList<ParticipantIdentityDto> participantIdentities) {
		this.participantIdentities = participantIdentities;
	}
	public String getGameVersion() {
		return gameVersion;
	}
	public void setGameVersion(String gameVersion) {
		this.gameVersion = gameVersion;
	}
	public String getPlatformId() {
		return platformId;
	}
	public void setPlatformId(String platformId) {
		this.platformId = platformId;
	}
	public String getGameMode() {
		return gameMode;
	}
	public void setGameMode(String gameMode) {
		this.gameMode = gameMode;
	}
	public int getMapId() {
		return mapId;
	}
	public void setMapId(int mapId) {
		this.mapId = mapId;
	}
	public String getGameType() {
		return gameType;
	}
	public void setGameType(String gameType) {
		this.gameType = gameType;
	}
	public ArrayList<TeamStatsDto> getTeams() {
		return teams;
	}
	public void setTeams(ArrayList<TeamStatsDto> teams) {
		this.teams = teams;
	}
	public ArrayList<ParticipantDto> getParticipants() {
		return participants;
	}
	public void setParticipants(ArrayList<ParticipantDto> participants) {
		this.participants = participants;
	}
	public long getGameDuration() {
		return gameDuration;
	}
	public void setGameDuration(long gameDuration) {
		this.gameDuration = gameDuration;
	}
	public long getGameCreation() {
		return gameCreation;
	}
	public void setGameCreation(long gameCreation) {
		this.gameCreation = gameCreation;
	}
}
